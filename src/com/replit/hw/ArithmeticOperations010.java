package com.replit.hw;

public class ArithmeticOperations010 {

	public static void main(String[] args) {
		/*
		 * Declare 2 numbers and assign values 200 and 100 respectively.
		 * 
		 * First, add numbers, then subtract, third multiply, forth divide.
		 * 
		 * Please use variables to print the values of each operation result on a
		 * separate line.
		 * 
		 */

		int num = 200;
		int num1 = 100;

		int sum = num + num1;
		int sub = num - num1;
		int multi = num * num1;
		int div = num / num1;

		System.out.println(sum);
		System.out.println(sub);
		System.out.println(multi);
		System.out.println(div);

	}

}
