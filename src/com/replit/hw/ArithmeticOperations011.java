package com.replit.hw;

public class ArithmeticOperations011 {

	public static void main(String[] args) {
		/*
		 * Write a program to print the perimeter and area of a rectangle with width = 5
		 * and height = 8.
		 */

		int width = 5;
		int height = 8;

		int perimeter = 2 * (width + height);
		int area = width * height;

		System.out.println("The perimeter of a rectangle with width " + width + " and height " + height
				+ " is equals to " + perimeter + " and the area equals to " + area);

	}

}
